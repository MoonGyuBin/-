def cal(num1, num2, operator):
    if operator == "+":
        result1 = num1 + num2
        print(result1)
    elif operator == "-":
        result2 = num1 - num2
        print(result2)
    elif operator == "*":
        result3 = num1 * num2
        print(result3)
    elif operator == "/":
        result4 = num1 / num2
        print(result4)